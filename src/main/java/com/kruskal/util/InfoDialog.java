package com.kruskal.util;

import javafx.scene.control.Alert;
import javafx.scene.control.TextArea;
import javafx.scene.layout.VBox;
import javafx.scene.text.Text;

public class InfoDialog {

    public static void show() {
        Alert alert = new Alert(Alert.AlertType.INFORMATION);
        alert.setTitle("Справка");
        alert.setHeaderText("Визуализация алгоритма Краскала");
        alert.getDialogPane().setPrefWidth(600);
        alert.getDialogPane().setPrefHeight(500);

        String infoText = """
                Назначение программы
                          --------------------
                          Программа предназначена для построения минимального остовного дерева
                          неориентированного взвешенного графа с помощью алгоритма Краскала.
                          Поддерживается создание, загрузка, сохранение и визуализация графов.
                
                          Работа с программой
                          --------------------
                
                          Add Node
                              Добавляет новую вершину в граф по клику на холсте.
                
                          Delete Node
                              Удаляет выбранную вершину вместе со всеми инцидентными ей рёбрами.
                
                          Add Edge
                              Добавляет ребро между двумя вершинами.
                              Для этого выберите первую вершину, затем вторую;
                              будет предложено ввести вес ребра.
                
                          Delete Edge
                              Удаляет выбранное ребро.
                
                          Edit Weight
                              Изменяет вес выбранного ребра.
                
                          Generate Graph
                              Генерирует случайный связный взвешенный граф.
                              Доступна настройка количества вершин и рёбер
                              через кнопку "Generate Params".
                
                          Insert Graph
                              Загружает граф из текстового файла в формате с координатами (см. ниже).
                
                          Save Graph
                              Сохраняет текущий граф в текстовый файл.
                
                          Run Kruskal Auto
                              Автоматически выполняет алгоритм Краскала с заданной скоростью.
                              Во время воспроизведения доступны пауза и изменение скорости.
                
                          Speed (ms)
                              Задаёт скорость автоматической визуализации в миллисекундах
                              между шагами.
                
                          Run Kruskal Manual
                              Запускает пошаговое выполнение алгоритма.
                              После запуска доступны переходы Prev / Next.
                
                          Prev Step
                              Переход к предыдущему шагу алгоритма.
                
                          Next Step
                              Переход к следующему шагу алгоритма.
                
                          Clean
                              Очищает оба холста и журнал действий.
                
                          Info
                              Открывает данную справку.
                
                          Формат файлов
                          --------------------
                          Программа использует единый формат с координатами вершин.
                
                          Первая строка:
                              COORDS
                
                          Вторая строка:
                              количество вершин
                
                          Далее:
                              id x y    (для каждой вершины)
                
                          Пустая строка
                
                          Далее:
                              вершина1 вершина2 вес    (для каждого ребра)
                
                          Пример:
                          COORDS
                          5
                          0 100 120
                          1 200 300
                          2 350 170
                          3 400 500
                          4 600 250
                
                          0 1 5
                          1 2 7
                          2 4 3
                          3 4 10
                
                          Алгоритм Краскала
                          --------------------
                          Алгоритм используется для построения минимального остовного дерева
                          связного взвешенного неориентированного графа.
                
                          Порядок работы алгоритма:
                
                          1. Все рёбра сортируются по возрастанию веса.
                
                          2. Рёбра рассматриваются по одному,
                             начиная с ребра минимального веса.
                
                          3. Если очередное ребро не образует цикл,
                             оно включается в минимальное остовное дерево.
                
                          4. Если добавление ребра приводит к образованию
                             цикла, оно пропускается.
                
                          5. Алгоритм завершается после добавления
                             (N − 1) рёбер, где N — количество вершин графа,
                             или после обработки всех рёбер.
                
                          Результатом работы является минимальное остовное дерево
                          (или лес, если граф несвязный), содержащее все вершины
                          графа и имеющее минимальную суммарную стоимость.
                """;

        TextArea textArea = new TextArea(infoText);
        textArea.setEditable(false);
        textArea.setWrapText(true);
        textArea.setPrefHeight(400);
        textArea.setStyle("-fx-font-family: monospace; -fx-font-size: 12px;");

        alert.getDialogPane().setContent(textArea);
        alert.showAndWait();
    }
}